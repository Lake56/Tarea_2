class Empleado{
    private String id;
    private String Apellidos;
    private String Nombre;
    private String Correo;
    public Empleado(String nombre, String apellidos, String correo, String id){
        this.Nombre=nombre;
        this.Apellidos=apellidos;
        this.Correo=correo;
        this.id=id;
    }
}
